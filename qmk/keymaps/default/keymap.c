enum custom_keycodes {
    EMACS = SAFE_RANGE,
};

static bool aorazer_os_is_mac = false;
static bool emacs_held = false;

bool process_detected_host_os_user(os_variant_t detected_os) {
    if (detected_os == OS_MACOS || detected_os == OS_IOS) {
        aorazer_os_is_mac = true;
    } else if (detected_os == OS_WINDOWS || detected_os == OS_LINUX) {
        aorazer_os_is_mac = false;
    }
    return true;
}

static bool send_emacs_combo(uint16_t keycode, keyrecord_t *record) {
    if (!record->event.pressed) {
        return true;
    }

    switch (keycode) {
        case KC_A:
            tap_code(KC_HOME);
            return false;
        case KC_E:
            tap_code(KC_END);
            return false;
        case KC_H:
            tap_code(KC_BSPC);
            return false;
        case KC_K:
            tap_code16(S(KC_END));
            tap_code(KC_DEL);
            return false;
        case KC_B:
            tap_code(KC_LEFT);
            return false;
        case KC_F:
            tap_code(KC_RGHT);
            return false;
        case KC_P:
            tap_code(KC_UP);
            return false;
        case KC_N:
            tap_code(KC_DOWN);
            return false;
        default:
            return true;
    }
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (emacs_held && send_emacs_combo(keycode, record) == false) {
        return false;
    }

    switch (keycode) {
        case EMACS:
            if (record->event.pressed) {
                emacs_held = true;
                register_code(aorazer_os_is_mac ? KC_LGUI : KC_LCTL);
            } else {
                unregister_code(aorazer_os_is_mac ? KC_LGUI : KC_LCTL);
                emacs_held = false;
            }
            return false;
    }

    return true;
}

